#include "3-calc.h"


/**
 * op_add - function returns the sum of 'a' and 'b'
 * @a: number
 * @b: number
 * Return: returns sum of a and b
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - function returns the sub of 'a' and 'b'
 * @a: number
 * @b: number
 * Return: returns sub of a and b
 */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - function returns the mul of 'a' and 'b'
 * @a: number
 * @b: number
 * Return: returns mul of a and b
 */

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - function returns the div of 'a' and 'b'
 * @a: number
 * @b: number
 * Return: returns div of a and b
 */

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - function returns the mod of 'a' and 'b'
 * @a: number
 * @b: number
 * Return: returns mod of a and b
 */

int op_mod(int a, int b)
{
	return (a % b);
}
